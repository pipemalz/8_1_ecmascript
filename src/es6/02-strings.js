//Template literals

let string = 'This text is a string';
console.log(string);

let phrasev1 = string + ' and i love it';

let phrasev2 = `${string} and i love it`;

console.log(phrasev1);
console.log(phrasev2);

//Multi-Line strings

let lorem = 'esto es un string \n esto es otra linea';
console.log(lorem);

let lorem2 = `esto es un string
esto es otra linea`;
console.log(lorem2);